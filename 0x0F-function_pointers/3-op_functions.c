#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"
/**
 * op_add - function that adds two integers
 * @a: the first integer
 * @b: the second integer
 *
 * Return: the result of the addition
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function that subtracts two integers
 * @a: the first integer
 * @b: the second integer
 *
 * Return: the result of the subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function that multiplies two integers
 * @a: the first number
 * @b: the second number
 *
 * Return: the result of the multiplication
 */
int op_mul(int a, int b);
{
	return (a * b);
}
/**
 * op_div - function that divides two integers
 * @a: the first number
 * @b: the second number
 *
 * Return: the result of the division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function that performs modulo operatio
 * @a: the first number
 * @b: the sevond number
 *
 * Return: the result of the modulo operation
 */
int op_mod(int a, int b)
{	
	return (a % b);
}
